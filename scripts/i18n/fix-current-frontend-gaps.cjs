function setNested(target, key, value) {
  const parts = key.split(".");
  let cursor = target;
  for (const part of parts.slice(0, -1)) {
    if (!cursor[part] || typeof cursor[part] !== "object") {
      cursor[part] = {};
    }
    cursor = cursor[part];
  }
  cursor[parts[parts.length - 1]] = value;
}

function applyTranslations(language, data) {
  let changed = 0;

  for (const [key, values] of Object.entries(SHARED_TRANSLATIONS)) {
    if (Object.prototype.hasOwnProperty.call(values, language)) {
      setNested(data, key, values[language]);
      changed += 1;
    }
  }

  for (const [key, values] of Object.entries(EXTRA_SHARED)) {
    if (Object.prototype.hasOwnProperty.call(values, language)) {
      setNested(data, key, values[language]);
      changed += 1;
    }
  }

  if (language === "ar") {
    for (const [key, value] of Object.entries(ARABIC_TRANSLATIONS)) {
      setNested(data, key, value);
      changed += 1;
    }
  }

  return changed;
}

for (const language of LANGUAGES) {
  const filePath = path.join(LOCALE_DIR, language, "translation.json");
  const data = JSON.parse(fs.readFileSync(filePath, "utf8"));
  const changed = applyTranslations(language, data);

  fs.writeFileSync(filePath, `${JSON.stringify(data, null, 2)}\n`, "utf8");
  console.log(`${language}: wrote ${changed} translation values`);
}
