def set_nested(d, keys, value):
    """Set a nested key in a dict, creating intermediate dicts as needed."""
    for key in keys[:-1]:
        if key not in d:
            d[key] = {}
        d = d[key]
    d[keys[-1]] = value

def main():
    for lang in LANGUAGES:
        fp = os.path.join(BASE, lang, "translation.json")
        with open(fp, "r", encoding="utf-8") as f:
            data = json.load(f)

        changes = []
        for full_key, translations in TRANSLATIONS.items():
            parts = full_key.split(".")
            if lang in translations:
                set_nested(data, parts, translations[lang])
                changes.append(f"  [OK] {full_key}")

        with open(fp, "w", encoding="utf-8") as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
            f.write("\n")

        print(f"[{lang}] Updated keys:")
        for c in changes:
            print(c)

    print("\nAll 14 language files updated successfully!")

if __name__ == "__main__":
    main()